// Flattens a language entry into the {{placeholder}} map used by the templates.
export function templateVars(code) {
  const l = LANGS[code];
  return {
    lang: code.replace("_", "-"),
    dir: l.dir,
    fontHref: l.fontHref,
    fontHead: l.fontHead,
    fontBody: l.fontBody,
    raw: `../raw/${code}`,
    ...l.demo,
    ...l.tiles,
  };
}

export function renderTemplate(html, vars) {
  return html.replace(/\{\{(\w+)\}\}/g, (_, key) => {
    if (!(key in vars)) throw new Error(`missing template var: ${key}`);
    return vars[key];
  });
}
